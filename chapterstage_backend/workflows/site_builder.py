_SCRIPT = """(function () {
  "use strict";
  var b64 = document.getElementById("storyboard-data").textContent.trim();
  var bin = atob(b64);
  var bytes = new Uint8Array(bin.length);
  for (var j = 0; j < bin.length; j++) { bytes[j] = bin.charCodeAt(j); }
  var data = JSON.parse(new TextDecoder("utf-8").decode(bytes));
  var scenes = (data && data.scenes) || [];
  var i = 0;

  var sceneEl = document.getElementById("scene");
  var prev = document.getElementById("prev");
  var next = document.getElementById("next");
  var bar = document.getElementById("bar");
  var counter = document.getElementById("counter");

  function el(tag, cls, text) {
    var n = document.createElement(tag);
    if (cls) n.className = cls;
    if (text != null) n.textContent = text;
    return n;
  }

  function renderQuiz(s) {
    var q = el("p", "body", s.question || s.title || "Check your understanding:");
    sceneEl.appendChild(q);
    var ul = el("ul", "options");
    var answered = false;
    (s.options || []).forEach(function (opt, idx) {
      var li = document.createElement("li");
      var b = el("button", "opt", opt);
      b.type = "button";
      b.addEventListener("click", function () {
        if (answered) return;
        answered = true;
        var correct = idx === s.answer_index;
        b.classList.add(correct ? "correct" : "wrong");
        if (!correct) {
          var btns = ul.querySelectorAll(".opt");
          if (btns[s.answer_index]) btns[s.answer_index].classList.add("correct");
        }
        var fb = el("p", "feedback " + (correct ? "ok" : "bad"),
          correct ? "Correct." : "Not quite \\u2014 the highlighted answer is right.");
        sceneEl.appendChild(fb);
      });
      li.appendChild(b);
      ul.appendChild(li);
    });
    sceneEl.appendChild(ul);
  }

  function render() {
    var s = scenes[i] || {};
    sceneEl.innerHTML = "";
    sceneEl.appendChild(el("span", "tag", s.type || "scene"));
    sceneEl.appendChild(el("h2", null, s.title || ("Scene " + (i + 1))));
    if (s.type === "quiz" && s.options) {
      renderQuiz(s);
    } else if (s.body) {
      sceneEl.appendChild(el("p", "body", s.body));
    }
    var pct = scenes.length ? Math.round(((i + 1) / scenes.length) * 100) : 0;
    bar.style.width = pct + "%";
    counter.textContent = "Scene " + (i + 1) + " of " + scenes.length;
    prev.disabled = i === 0;
    next.disabled = i >= scenes.length - 1;
    sceneEl.focus();
  }

  prev.addEventListener("click", function () { if (i > 0) { i--; render(); } });
  next.addEventListener("click", function () {
    if (i < scenes.length - 1) { i++; render(); }
  });
  document.addEventListener("keydown", function (e) {
    if (e.key === "ArrowRight") next.click();
    if (e.key === "ArrowLeft") prev.click();
  });

  render();
})();
"""


def _metadata_json(storyboard, pack, verdict, meta) -> str:
    """Every METADATA_KEYS field the validator requires, populated from the run."""
    payload = {
        "experience_id": meta.get("experience_id", ""),
        "job_id": meta.get("job_id", ""),
        "book_title": meta.get("book_title", ""),
        "chapter_title": meta.get("chapter_title", storyboard.get("title", "")),
        "audience_level": storyboard.get("audience_level", "beginner"),
        "experience_style": storyboard.get("experience_style", "visual_story"),
        "screen_count": storyboard.get("screen_count", len(storyboard.get("scenes", []))),
        "band_room_id": meta.get("band_room_id", ""),
        "selected_brainstorm_variant": meta.get("selected_brainstorm_variant", ""),
        "faithfulness_score": verdict.get("faithfulness_score", 0.0),
        "engagement_score": verdict.get("engagement_score", 0.0),
        "created_at": meta.get("created_at", ""),
    }
    return json.dumps(payload, indent=2, ensure_ascii=False)
