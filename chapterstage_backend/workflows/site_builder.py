"""site_builder.py — storyboard envelope -> the four required site files.

This is the node the spec named (§6.2 run_visual_builder_agent / publish) and the
repo was missing: it turns the visual stage's storyboard into a self-contained,
SANDBOX-SAFE mini-site — index.html, styles.css, script.js, metadata.json — that
passes app.services.site_validator (the §7.5/§11 gate) by construction:

  - CSP meta is imported VERBATIM from the validator (STRICT_CSP_HEADER) so the
    declared policy can never drift from the enforced one.
  - script-src 'self' => zero inline executable JS; all behavior is in script.js,
    events bound via addEventListener (NO on* handlers the validator forbids).
  - connect-src 'none' + no fetch/XHR/WebSocket/eval/Function => no egress, no
    dynamic code. Scene data rides an inert <script type="application/json"> block
    (data, not code) with '<' escaped so chapter text can't break out of it.
  - viewport meta + local-only refs (styles.css, script.js) => mobile + offline.

build_site(storyboard, pack, verdict, meta) -> {filename: text}. Pure stdlib.
"""
from __future__ import annotations

import base64
import html
import json
import sys
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parent.parent))
from app.services.site_validator import STRICT_CSP_HEADER  # noqa: E402


def build_site(storyboard: dict, pack: dict, verdict: dict, meta: dict) -> dict:
    """Render the four files as a {name: content} dict. `meta` supplies the
    metadata.json identity fields (experience_id, job_id, band_room_id, created_at,
    book/chapter titles)."""
    scenes = storyboard.get("scenes", []) or []
    title = storyboard.get("title") or meta.get("chapter_title") or "Chapter"

    return {
        "index.html": _index_html(title, scenes),
        "styles.css": _STYLES,
        "script.js": _SCRIPT,
        "metadata.json": _metadata_json(storyboard, pack, verdict, meta),
    }


def _esc(s) -> str:
    return html.escape(str(s), quote=True)


def _index_html(title: str, scenes: list) -> str:
    # Scene data as a BASE64 island. Why not raw JSON: chapter text legitimately
    # contains 'fetch(' / 'eval(' / '<script>' (CS books!), which would either
    # break out of the <script> block or trip the validator's denylist as a false
    # positive. base64 is [A-Za-z0-9+/=] only — no '<', no forbidden token, no
    # quote to escape. script.js atob+TextDecoder+JSON.parse decodes it (all
    # CSP-safe; no eval). Rendered via textContent, so content is inert (no XSS).
    payload = json.dumps({"title": title, "scenes": scenes}, ensure_ascii=False)
    data = base64.b64encode(payload.encode("utf-8")).decode("ascii")
    total = len(scenes)
    return """<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="utf-8">
<meta name="viewport" content="width=device-width, initial-scale=1">
<meta http-equiv="Content-Security-Policy" content="%s">
<title>%s</title>
<link rel="stylesheet" href="styles.css">
</head>
<body>
<main id="app" aria-live="polite">
  <header>
    <h1>%s</h1>
    <div id="progress" class="progress"><div id="bar" class="bar"></div></div>
    <p id="counter" class="counter"></p>
  </header>
  <section id="scene" class="scene" tabindex="-1"></section>
  <nav class="controls">
    <button id="prev" type="button" aria-label="Previous scene">&larr; Back</button>
    <button id="next" type="button" aria-label="Next scene">Next &rarr;</button>
  </nav>
  <footer><p class="made">Generated by ChapterStage &middot; %d scenes</p></footer>
</main>
<script id="storyboard-data" type="application/json">%s</script>
<script src="script.js"></script>
</body>
</html>
""" % (STRICT_CSP_HEADER, _esc(title), _esc(title), total, data)


